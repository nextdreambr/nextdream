import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';

const SOURCE_URL = 'https://servicodados.ibge.gov.br/api/v1/localidades';
const STATES_URL = `${SOURCE_URL}/estados?orderBy=sigla`;
const OUTPUT_FILE = fileURLToPath(new URL('../src/app/data/brazilCities.ts', import.meta.url));
const REFERENCE_DATE = process.env.BRAZIL_CITIES_REFERENCE_DATE ?? new Date().toISOString().slice(0, 10);
const collator = new Intl.Collator('pt-BR');

async function fetchJson(url) {
  const response = await fetch(url);

  if (!response.ok) {
    throw new Error(`Failed to fetch ${url}: ${response.status} ${response.statusText}`);
  }

  return response.json();
}

function tsString(value) {
  return JSON.stringify(value);
}

function renderState(state) {
  const cities = state.cities.map((city) => `      ${tsString(city)},`).join('\n');

  return `  {
    uf: ${tsString(state.uf)},
    name: ${tsString(state.name)},
    cities: [
${cities}
    ],
  },`;
}

async function main() {
  const statesResponse = await fetchJson(STATES_URL);
  const states = statesResponse
    .map((state) => ({
      uf: state.sigla,
      name: state.nome,
    }))
    .sort((a, b) => collator.compare(a.uf, b.uf));

  const statesWithCities = await Promise.all(
    states.map(async (state) => {
      const municipalitiesUrl = `${SOURCE_URL}/estados/${state.uf}/municipios`;
      const municipalities = await fetchJson(municipalitiesUrl);
      const cities = municipalities
        .map((municipality) => municipality.nome)
        .sort((a, b) => collator.compare(a, b));

      if (cities.length === 0) {
        throw new Error(`IBGE returned no municipalities for ${state.uf}`);
      }

      return {
        ...state,
        cities,
      };
    }),
  );
  const totalCities = statesWithCities.reduce((total, state) => total + state.cities.length, 0);

  const content = `// Generated by scripts/generate-brazil-cities.mjs from official IBGE locality APIs.
// Do not edit manually; rerun the generator to refresh the dataset.

export interface BrazilState {
  uf: string;
  name: string;
  cities: string[];
}

export const BRAZIL_CITIES_SOURCE = 'IBGE';
export const BRAZIL_CITIES_SOURCE_URL = '${SOURCE_URL}';
export const BRAZIL_CITIES_REFERENCE_DATE = '${REFERENCE_DATE}';
export const BRAZIL_CITIES_TOTAL = ${totalCities};

export const BRAZIL_STATES: BrazilState[] = [
${statesWithCities.map(renderState).join('\n')}
];

export function getBrazilState(uf?: string) {
  const normalizedUf = uf?.toUpperCase();
  return BRAZIL_STATES.find((state) => state.uf === normalizedUf);
}

export function getCitiesForState(uf?: string) {
  return getBrazilState(uf)?.cities ?? [];
}
`;

  await writeFile(OUTPUT_FILE, content);
  console.log(`Generated ${OUTPUT_FILE} with ${statesWithCities.length} states and ${totalCities} cities.`);
}

await main();
